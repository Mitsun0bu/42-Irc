void	Server::quitCmd(User &user, std::vector<std::string> &cmdTokens)
{
	std::string msg = ":Quit";

	for (size_t i = 1; i < cmdTokens.size(); i++)
		msg.append(cmdTokens[i]);
	printRecvError(user.getSocket());
	cmdReply(user, "QUIT", msg);
	logoutUser(user);
}