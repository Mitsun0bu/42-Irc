void	Server::searchForData(void)
{
	for (int fd = 0; fd <= this->_clientFdList.max; fd++)
	{
		if (FD_ISSET(fd, &this->_clientFdList.read))
		{
			if (fd == this->_socket)
				this->acceptNewUser();
			else
				this->handleClientData(fd);
		}
	}
}